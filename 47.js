//function with parameters

function add(a,b){
    console.log(a+b);
}
add(2,3) //5

// ----------------------------------------------------------------

function showName(user){
    console.log(`welcome ${user}`)
}

showName('prajakta')  //welcome prajakta 

// user --> argument (passed at the time of function defining)
// prajakta --> parameter (passed at the time of function calling)
