//swap without using third variable
let a=5
let b=10

console.log('before swapping');
console.log('a : ',a);
console.log('b : ',b);

a=a+b   //15
b=a-b   //5
a=a-b   //10

console.log('after swaping');
console.log('a : ',a);
console.log('b : ',b);


/*
before swaping
a :  5
b :  10
after swaping
a :  10
b :  5
*/
