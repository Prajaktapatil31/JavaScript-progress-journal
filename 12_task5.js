//swap with using third variable

let a=10
let b=5

console.log('before swapping');
console.log('a : ',a);
console.log('b : ',b);

let c=a
a=b
b=c

console.log('after swaping');
console.log('a : ',a);
console.log('b : ',b);


/*

before swapping
a :  10
b :  5
after swaping
a :  5
b :  10

*/