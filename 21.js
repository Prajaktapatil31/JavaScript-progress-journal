let action = Number(prompt(`
        ATM Action Menu
    ------------------------   
    
    1. Check Balance
    2. Withdraw 
    3. Deposit
    4. Exit
`))

switch(action){
    case 1: console.log('Check Balance'); break;
    case 1: console.log('Withdraw '); break;
    case 1: console.log('Deposit'); break;
    case 1: console.log('Exit'); break;
    default: console.log('invalid action choosen');
        
}