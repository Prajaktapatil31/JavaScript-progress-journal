/* --------- Objects & Destructuring in JavaScript ------------

An Object is a collection of key-value pairs. (it is like a datatype)
Keys are always(mostly) strings (or symbols), values can be any data type.
*/      

const data={
    name: 'prajakta',
    age:21,
    isStudent:true
}

console.log(data);     //{ name: 'prajakta', age: 21, isStudent: true }
console.log(data.name);     //prajakta